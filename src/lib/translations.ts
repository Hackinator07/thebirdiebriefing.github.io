export function getTranslation(langCode: string, key: keyof TranslationKeys): string {
  const lang = translations[langCode] || translations.en;
  return lang[key];
}

export function getCurrentLanguage(): string {
  if (typeof window === 'undefined') return 'en';
  return localStorage.getItem('jss-pref') || 'en';
}
